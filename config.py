CHARGING_RADIUS = 2.7       # 充电/采集半径 rho (m)
 # 自定义，论文中未指定
CHARGING_ALPHA = 0.1        # 充电效率衰减参数 alpha (可调，基于常见模型)
CHARGING_BETA = 2.0         # 充电效率衰减指数 beta (可调，基于常见模型)
def charging_efficiency(d):
    """ 计算充电效率 u(d)，基于距离 d 的递减函数 (论文模型描述) """
    rho = CHARGING_RADIUS
    if d > rho:
        return 0.0
    return 1 / (CHARGING_ALPHA + (d / rho) ** CHARGING_BETA)  # 示例公式，确保 0 <= u <= 1

